# 3. Función para calcular cobertura por región
def calcular_cobertura_region(df, tecnologias, region, departamentos_region):
    datos_region = df[df['DEPARTAMENTO'].isin(departamentos_region)]
    cobertura_region = {}

    for tech in tecnologias:
        total_registros = len(datos_region)
        if total_registros > 0:
            con_cobertura = len(datos_region[datos_region[tech] == 'S'])
            porcentaje = (con_cobertura / total_registros) * 100
            cobertura_region[tech] = porcentaje
        else:
            cobertura_region[tech] = 0

    return cobertura_region

