columnas_cobertura = [col for col in df_final.columns if 'COBERT' in col.upper() or 'COPERT' in col.upper()]

# Calcular porcentaje de cobertura por departamento
resultados = []

for departamento in df_final['DEPARTAMENTO'].unique():
    datos_depto = df_final[df_final['DEPARTAMENTO'] == departamento]
    total_registros = len(datos_depto)

    if total_registros > 0:
        depto_data = {'DEPARTAMENTO': departamento, 'TOTAL_REGISTROS': total_registros}

        for tech in columnas_cobertura:
            con_cobertura = len(datos_depto[datos_depto[tech] == 'S'])
            porcentaje = (con_cobertura / total_registros) * 100
            depto_data[tech] = porcentaje

        resultados.append(depto_data)

# Convertir a DataFrame
df_resultados = pd.DataFrame(resultados)

